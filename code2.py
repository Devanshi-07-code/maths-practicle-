import numpy as np

# Create a vector (1D array)
v = np.array([1, 2, 3])
print("Vector v:\n", v)

# Create a column vector (2D array)
v_col = np.array([[1], [2], [3]])
print("\nColumn vector v_col:\n", v_col)

# Create a matrix (2D array)
A = np.array([[1, 2, 3],
              [4, 5, 6]])
print("\nMatrix A:\n", A)


